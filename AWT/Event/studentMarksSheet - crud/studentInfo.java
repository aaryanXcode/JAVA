public class studentInfo
{
	int Rollno;
	int physicsMarks;
	int chemistryMarks;
	int mathMarks;
	int totalMarks;
	int percentage;
	String division;
	studentInfo(int roll,int physics,int chemistry,int math,int total,int per,String div)
	{
		Rollno=roll;
		physicsMarks=physics;
		chemistryMarks=chemistry;
		mathMarks=math;
		totalMarks=total;
		percentage=per;
		division=div;
	}
}
		
	
	  